package operator;

public class Q9021 {
	public static void main(String[] args) {
		int a = 10, b = 10;

		System.out.println("최초값 a = " + a + ", b = " + b);
		System.out.println();

		System.out.println("a++ = " + a++ + ", ++b = " + ++b);
		System.out.println("실행후 a = " + a + ", b = " + b);
		System.out.println();

		System.out.println("a-- = " + a-- + ", --b = " + --b);
		System.out.println("실행후 a = " + a + ", b = " + b);
	}
}